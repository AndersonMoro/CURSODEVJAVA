package br.unoesc.atividades2506;

public class Calculadora {
	
	
	double soma (double n1, double n2) {
		double resp;
		resp = n1+n2;
		return resp;
				
	}
	double sub (double n1, double n2) {
		double resp;
		resp = n1-n2;
		return resp;
				
	}
	double mult (double n1, double n2) {
		double resp;
		resp = n1*n2;
		return resp;
				
	}
	double divisao (double n1, double n2) {
		double resp;
		resp = n1/n2;
		return resp;
				
	}

}
